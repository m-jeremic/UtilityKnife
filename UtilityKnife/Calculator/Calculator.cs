using System;

namespace UtilityKnife.Calculator
{
    public class Calculator
    {
        public static  double Add(double x, double y)
        {
            return x + y;
        }

        public static double Subtraction(double x, double y)
        {
            return x - y;
        }

        public static double Multiply(double x, double y)
        {
            return x * y;
        }
    }
}
